    private void createActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createActionPerformed
        Consultas query = new Consultas();
        String usuario = Emailregistro.getText();
        String password = Passwordregistro.getText();
        if (usuario.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Por favor, ingrese tanto el usuario como la contrase√±a.");
        }else{
            query.guardarUsuario(usuario, password);
            cleantext();
        }
    }//GEN-LAST:event_createActionPerformed

